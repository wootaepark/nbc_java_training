package week02;

public class W10 {

    // 연산중에 제일 빠르다 (비트연산자), 가장 작은 단위 이기 때문이다.

    // << (x2), >> (/2)
    public static void main(String[] args) {
        System.out.println(3<<2); // 12
        System.out.println(3<<1); // 6
        System.out.println(3>>2); // 0, 최좌측 비트 0으로 채워진다.


    }




}
